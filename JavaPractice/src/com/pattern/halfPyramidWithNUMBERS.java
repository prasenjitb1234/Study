package com.pattern;

import java.util.Scanner;

public class halfPyramidWithNUMBERS {

	public static void main(String[] args) {
		Scanner sc= new Scanner(System.in);
		
		// half pyramid with numbers 
		
		
		int n = 5;
		
		for(int i=1;i<=n;i++) {
			for(int j=1;j<=i;j++) {
				System.out.print(j+" ");
			}
			System.out.println();
		}
		
		
		
		
		
		
		
		
		
	}

}
